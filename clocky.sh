function tiktok
{
local tiktok=$1

local hours=${tiktok%:*}
local minutes=${tiktok#*:}
minutes=${minutes#0}


while : ; do
    $CLEAR
    centralizationalizer
    numbers=$( printf "%d:%02d" $hours $minutes )
    for number in $( echo $numbers | sed -e 's/./& /g' ); do
        set_lines_oOo
        render_character
    done
    tput cup 0 0
    sleep 3
    (( minutes += 1 ))
    if [[ $minutes -ge 60 ]]; then
        minutes=0
        (( hours += 1 ))
    fi

    if [[ $hours -gt 12 ]]; then
        hours=1
    fi
done
return
}


