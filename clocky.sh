#     echo -n _close_to_the_edge=$_close_to_the_edge \
#         x=$x \
#         x_check=$x_check \
#         y=$y
#----------------------------------------------------------------------#
fi

(( y = ( _close_to_the_edge * 16 ) + _y_init ))
for line in "${lines[@]}" ; do
